package com.example.bankappzup.Model;

public class ListItens {

    private String title,desc,date,value;



    public ListItens (String title,String desc, String date,String value){
      this.title = title;
      this.desc = desc;
      this.date = date;
      this.value = value;



    }

    public String getTitle() {
        return title;
    }



    public String getDesc() {
        return desc;
    }



    public String getDate() {
        return date;
    }



    public String getValue() {
        return value;
    }





}
